/*
 * Extracts the growth from the table(s) up above
 */
size_t extract_grow_from_defaults(int n, char* name,
				  double** ptable, double** prate)
{
  size_t rate_len = KAY1;
  int ptable_len = (int) pow(rate_len, n) + 1;
  int prate_len  = rate_len * n + 1;
  double *grow;
  double *rates;
  
  // Sanity check
  if (n != NUTRIENTS)
    e_quit("extract_grow_from_defaults: Only have n=3 available\n");

  // These output pointers are free'd in the calling function so we
  // need to allocate them here. I'm not sure why +1 but that's the
  // way its in the original code - see extract_grow(...) in ecofunct.c
  *ptable = (double*) calloc(ptable_len, sizeof(double));
  *prate  = (double*) calloc(prate_len,  sizeof(double));

  // Switch on name
  if (strcmp(name, "10benINP") == 0) {
    grow  = (double *)NORMGROW_ben;
    rates = (double *)RATES_ben;
  } else if (strcmp(name, "10plkINP") == 0) {
    grow  = (double *)NORMGROW_plk;
    rates = (double *)RATES_plk;
  } else {
    e_quit("extract_grow_from_defaults: Unknown name '%s'\n", name);
  }

  // Copy over
  memcpy(*ptable, grow,  (ptable_len-1)*sizeof(double));
  memcpy(*prate,  rates, (prate_len-1)*sizeof(double));

  return(rate_len);
}


